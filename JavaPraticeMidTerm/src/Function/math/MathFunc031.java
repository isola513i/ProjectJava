package Function.math;

public class MathFunc031 {
    public static int add(int add1, int add2) {
        return add1 + add2;
    }

    public static int substract(int sub1, int sub2) {
        return sub1 - sub2;
    }

    public static double multiply(double multi1, double multi2) {
        return multi1 * multi2;
    }

    public static double divide(double divide1, double divide2) {
        return divide1 / divide2;
    }

}
